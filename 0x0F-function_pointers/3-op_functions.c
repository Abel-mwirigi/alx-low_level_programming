#include "3-calc.h"

/**
 * op_add - addition
 * @a:first number
 * @b:second number
 * Return:Always 0
 */

int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - difference of numbers
 * @a:first number
 * @b:second number
 * Return:Always 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication
 * @a:first number
 * @b:second number
 * Return:Always 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a:first number
 * @b:second number
 * Return:Always 0
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns remainder
 * @a:first parameter
 * @b:second parameter
 * Return:Always 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
